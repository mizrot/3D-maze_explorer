static inline int cell_is_wall(int mx, int my) {
    if (mx < 0 || my < 0 || mx >= MAP_WIDTH || my >= MAP_HEIGHT) return 1; // treat OOB as wall
    return map[my][mx] != 0;
}

#define PLAYER_RADIUS 0.20f
static int can_stand_at(float x, float y) {
    // Inflate walls by PLAYER_RADIUS: check the 4 “corner” cells touching the circle
    int left   = (int)floorf(x - PLAYER_RADIUS);
    int right  = (int)floorf(x + PLAYER_RADIUS);
    int top    = (int)floorf(y - PLAYER_RADIUS);
    int bottom = (int)floorf(y + PLAYER_RADIUS);
    return !(
        cell_is_wall(left,  top)    ||
        cell_is_wall(right, top)    ||
        cell_is_wall(left,  bottom) ||
        cell_is_wall(right, bottom)
    );
}

static void move_with_collisions(Player *p, float dx, float dy) {
    // Try X, then Y (allows sliding along walls)
    float nx = p->x + dx;
    if (can_stand_at(nx, p->y)) p->x = nx;

    float ny = p->y + dy;
    if (can_stand_at(p->x, ny)) p->y = ny;
}

/**
 * main - entry point
 *
 * Return: 0 on success
 */
int main(void)
{
	SDL_Window *window = NULL;
	SDL_Renderer *renderer = NULL;
	/*Start in the middle of the map*/
	Player player = {1.5, 1.5, 90.0};

	initSDL(&window, &renderer);
	bool running = true;
	SDL_Event event;
while (running) {
    while (SDL_PollEvent(&event)) {
        if (event.type == SDL_QUIT) {
            running = false;
        } else if (event.type == SDL_KEYDOWN) {
            const float step = 0.20f;

            switch (event.key.keysym.sym) {
                case SDLK_w: {
                    float dx = step * cos(player.angle);
                    float dy = step * sin(player.angle);
                    move_with_collisions(&player, dx, dy);
                } break;
                case SDLK_s: {
                    float dx = -step * cos(player.angle);
                    float dy = -step * sin(player.angle);
                    move_with_collisions(&player, dx, dy);
                } break;
                case SDLK_d:
                    player.angle += 0.1f;   // still degrees (≈0.1°)
                    if (player.angle >= 360.0f) player.angle -= 360.0f;
                    break;
                case SDLK_a:
                    player.angle -= 0.1f;
                    if (player.angle < 0.0f) player.angle += 360.0f;
                    break;
            }
        }
    }

    drawWalls(renderer, player, map);
    /* ~60 fps cap */
    SDL_Delay(16);
}
	cleanupSDL(window, renderer);
	return (0);
}

