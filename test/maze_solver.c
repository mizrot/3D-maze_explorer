typedef struct { int x, y; } Node;

int solve_maze(const int m[MAP_HEIGHT][MAP_WIDTH], Node start,
               Node *out_path, int max_out) {
    static int parent[MAP_HEIGHT][MAP_WIDTH];
    static unsigned char seen[MAP_HEIGHT][MAP_WIDTH];
    memset(seen, 0, sizeof(seen));
    for (int y = 0; y < MAP_HEIGHT; ++y)
        for (int x = 0; x < MAP_WIDTH; ++x)
            parent[y][x] = -1;

    Node q[MAP_HEIGHT * MAP_WIDTH];
    int qh = 0, qt = 0;
    q[qt++] = start;
    seen[start.y][start.x] = 1;

    Node exit = (Node){-1, -1};
    const int dirs[4][2] = {{1,0},{-1,0},{0,1},{0,-1}};

    while (qh < qt) {
        Node p = q[qh++];
        if (m[p.y][p.x] == 2) { exit = p; break; }
        for (int i = 0; i < 4; ++i) {
            int nx = p.x + dirs[i][0], ny = p.y + dirs[i][1];
            if (nx < 0 || ny < 0 || nx >= MAP_WIDTH || ny >= MAP_HEIGHT) continue;
            if (seen[ny][nx] || !PASS(m[ny][nx])) continue;
            seen[ny][nx] = 1;
            parent[ny][nx] = p.y * MAP_WIDTH + p.x;
            q[qt++] = (Node){nx, ny};
        }
    }
    if (exit.x == -1) return 0; // no path

    int len = 0;
    for (Node cur = exit; ; ) {
        if (len < max_out) out_path[len] = cur;
        ++len;
        int pr = parent[cur.y][cur.x];
        if (pr == -1) break;
        cur = (Node){ pr % MAP_WIDTH, pr / MAP_WIDTH };
    }
    // reverse in-place up to max_out elements
    for (int i = 0; i < len / 2 && i < max_out; ++i) {
        Node t = out_path[i];
        out_path[i] = out_path[len - 1 - i];
        out_path[len - 1 - i] = t;
    }
    return len; // number of steps in shortest path
}
